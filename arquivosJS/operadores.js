// * Operadores * 

// Os operadores nos permitem manipular o valor das variáveis. realizar
// operações e comparar seus valores.

// * De atribuição * 
// Atribuem o valor à direita para a variável à esquerda.

let idade = 35; // Atribui o número 35 a idade.

// * Númericos * 
// Servem para realizar operações matemáticas

10 + 15
15 - 10
10 * 15
10 / 2 

//15++ // Incremento, é igual a 15 + 1 = 16
//15-- // Decremento, é igual a 15 - 1 = 14

15 % 5 // Módulo, retorna o resto da divisão. 15 % 5 = 0
15 % 2 // Módulo. retorna o resto da divisão = 1

// Os operadores númericos sempre retornarão o resultado númerico da operação que está sendo executada.


// * Concatenação * 
// Usado para unir strings, retorna outra string.

let nome = 'Ruan';
let sobrenome = 'Araújo';
let nomeCompleto = nome + ' ' + sobrenome;
console.log(nomeCompleto)

// Se misturarmos outros tipos de dados, eles são convertidos em strings.

let fileira = 'M';
let assento = 7;
let localizacao = fileira + assento;
console.log(localizacao)
